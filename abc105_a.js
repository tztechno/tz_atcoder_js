function Main(input) {
  const lines = input.trim().split('\n');
  const [A, B] = lines[0].split(' ').map(ele => parseInt(ele));
  console.log(A % B == 0 ? '0' : '1');
}

Main(require('fs').readFileSync('/dev/stdin', 'utf8'));
